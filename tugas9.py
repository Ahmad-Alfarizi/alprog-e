# Menghitung suhu celcius

suhu = float(input("Masukkan suhu celcius = "))

fahrentheit = (suhu * 9/5) + 32
print(f"{suhu} celcius = {fahrentheit} fahrenheit")

kelvin = suhu + 273,15
print(f"{suhu} celcius = {kelvin} kelvin")

reamur = suhu * (4/5)
print(f"{suhu} celcius = {reamur} reamur")