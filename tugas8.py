x = 24
y = 85

# Penjumlahan
hasil = x + y
print("nilai dari x + y = ", hasil)

# Pengurangan
hasil = x - y
print("hasil dari x - y = ", hasil)

# Pembagian 
hasil = x / y
print("hasil dari x / y = ", hasil)

# Perkalian
hasil = x * y
print("hasil dari x * y =", hasil)

# Eksponen
hasil = x ** y
print("hasil dari x ** y = ", hasil)

# Modulo
hasil = x % y
print("hasil dari x % y = ", hasil)

# Floor division
hasil = x // y
print("hasil dari x // y = ", hasil)

